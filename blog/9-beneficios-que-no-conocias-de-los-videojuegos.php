        <div id="textos2">
        
        Muy a pesar de lo que mamá y papá siempre han dicho respecto a que los videojuegos están
        relacionados con la violencia, además de perjudicar nuestra salud, estudios recientes han 
        demostrado que los videojuegos, traen consigo diversos beneficios psicológicos y hasta físicos, 
        a continuación te mostramos un listado con <strong>9 beneficios que los videojuegos pueden traer a tu vida</strong>.
        <br /><br />
        
<h1>Jugar Mario Bros resulta como asteroides para tu cerebro</h1>
<br />
        Para entender mejor como los videojuegos influyen en el funcionamiento del cerebro, investigadores 
        alemanes realizaron recientemente un estudio, en el cual se pidió a 23 adultos con una edad promedio 
        de 25 años, que jugaran Super Mario 64 30 minutos diarios por un periodo de 2 meses, mientras que un 
        grupo de control de características similares, no jugó videojuegos en lo absoluto.
<br />
		Los resultados fueron sorprendentes, pues al realizar una resonancia magnética a ambos grupos al final
         del periodo de prueba, el grupo de juego, <strong>tuvo un aumento de materia gris en el hipocampo derecho, la 
         corteza pre frontal derecha y el cerebelo, aéreas del cerebro responsables  de la navegación espacial, 
         la formación de memoria, la estrategia, la planeación y la motricidad en las manos</strong>.
<br />
		Al final del estudio, los investigadores llegaron a la conclusión de que los <strong>videojuegos podrían utilizarse 
         como terapia para la esquizofrenia, trastornos de estrés postraumático y el alzhéimer</strong>.
<br /><br />
<h1>Starcraft te hace más inteligente</h1>
<br />
        En agosto, investigadores británicos, descubrieron que algunos juegos de estrategia como starcraft, pueden aumentar
         la flexibilidad del cerebro del jugador, a lo que los científicos llaman la piedra angular de la inteligencia humana.
												<br />	
         Para el estudio, se realizaron pruebas psicológicas a 72 voluntarios después de haber jugado starcraft, o el juego de
          simulación Los sims durante 40 horas en un periodo de 6 a 8 semanas, al terminar la prueba, encontraron que los participantes
           que jugaron starcraft, mejoraron el rendimiento durante las pruebas psicológicas y la realización de tareas de habilidad
            cognitiva con mayor rapidez y precisión, <strong> dicho hallazgo pudiera ser utilizado para tratar síntomas relacionados con el TDAH
             o algunas lesiones cerebrales</strong>.                                        
                                                <br />	<br />			
					<center><img src="imagenes/Star.jpg" /></center>
<br />
<br />
<h1>Los videojuegos pueden retrasar el proceso de envejecimiento</h1>
<br />
        Realizar ejercicios de habilidad mental dos horas a la semana, puede ayudar a reducir el grado de deterioro mental relacionado con el
        envejecimiento, según un estudio reciente realizado en la universidad de Iowa a 681 personas sanas mayores de 50 años , el cual revelo que 
        jugar durante 10 horas un juego especialmente diseñado, <strong>se pudo disminuir el declive natural de las habilidades cognitivas de hasta 7 años 
        en algunos casos</strong>.
<br />
<br />
<h1>Pueden ayudar a los niños disléxicos a leer mejor</h1>
<br/>
        Un estudio realizado por la universidad de Padua demostró que es mentira que los videojuegos son malos para el cerebro de los niños pequeños, en 
        febrero los investigadores italianos, presentaron evidencia  de que jugar videojuegos de ritmo rápido puede mejorar las habilidades de lectura de 
        niños con dislexia.
        
        <br />
        Para el estudio niños de entre 7 y 13 años fueron separados en dos grupos, uno de los cuales jugó un juego de acción llamado “Rayman raving ravids”,
         mientras que el otro jugo un juego más lento, posteriormente cuando las habilidades de los niños fueron evaluadas nuevamente, los que jugaron el juego de acción,
          fueron capaces de leer más rápido  y con mayor precisión, <strong>la hipótesis de los investigadores se basa en que este tipo de juegos aumenta su capacidad de atención,
           habilidad crucial para la lectura</strong>.
													<br /><br />
							<center>	<img src="imagenes/Adultos.jpg" />	</center>
<br/>
<br/>
<h1>Jugadores adolecentes realizan mejor una cirugía virtual que médicos residentes reales</h1>
<br/>
Científicos de la universidad de Texas medical branch en Galveston, llevaron a cabo un experimento en el cual jugadores de nivel de secundaria, jugadores universitarios y 
médicos residentes, compitieron para ver quién podía obtener mejores resultados durante una cirugía virtual llevada a cabo en un simulador, sorprendentemente, los jóvenes 
de secundaria quienes jugaban un promedio de 2 horas diarias, obtuvieron mejores resultados que los médicos profesionales, los cuales juegan muy poco, o nada.
<br/>
<br/>
<h1>Pueden ser un calmante para el dolor</h1>
<br/>
Durante la Reunión anual de la asociación americana del dolor durante el 2010, se presento evidencia de que los videojuegos, en especial los de realidad virtual, resultan eficaces
 en la reducción de la ansiedad y el dolor durante el tratamiento de algunas enfermedades crónicas reduciendo en algunos casos, las tasas de dolor de un 30% hasta un 50%, la explicación 
 que proporcionaron, es que <strong>al estar inmersos en un mundo de juego virtual, el cerebro permanece ocupado utilizando otros sentidos en lugar de centrarse en el dolor, además de liberar 
 endorfinas, sustancia química asociada con la felicidad y la capacidad de adormecer el malestar</strong>.
														<br /><br />
						
<br/>
<br/>
<h1>Call of duty puede mejorar la vista </h1>
<br/>
Durante el 2009 se realizo un estudio, en el cual se separaron dos grupos, uno de los cuales jugó algunos juegos de disparos en primera persona, mientras que el otro grupo jugó “Los sims”. 
Como resultado el primer grupo demostró un <strong>aumento en la sensibilidad al contraste  o en los cambios para discernir los cambios sutiles de brillo en una imagen</strong>.
<br/>
<br/>
							<center>	<img src="imagenes/call.jpg" />	</center>
<br/>
<br/>                            
<h1>Pueden ser tan eficaces como una terapia psicológica</h1>
<br/>
Durante el 2012 investigadores neozelandeses realizaron un estudio a 168 adolecentes con edad promedio de 15 años, los cuales previamente habían pedido ayuda o se encontraban en proceso 
de lucha contra la depresión, la mitad del grupo se sometió a terapia tradicional, mientras que la otra mitad jugo un juego especialmente diseñado llamado SPARX en el cual los pacientes crean 
avatares, los cuales deben aplastar los pensamientos negativos, y restaurar el orden del mundo virtual.
<br/>
Los resultados para el grupo de los pacientes que jugaron SPARX fueron bastante alentadores, <strong>pues el 44 porciento se recupero totalmente de la depresión mientras que del otro grupo solo un 
26 peciento pudo hacerlo por completo</strong>.
<br/><br/>
<h1>Pueden ayudar a víctimas de accidentes a recuperar el movimiento</h1>
<br/>
Se separaron en dos grupos personas que habían tenido algún accidente cerebro bascular hace 7 años, uno de los grupos realizo los ejercicios de rehabilitación tradicionales para recuperar 
la movilidad, mientras que el otro grupo se dedico a jugar video juegos, aunque ambos grupos mostraron mejorías en el agarre, solo el grupo de los videojuegos mostro mejoría en la recuperación de 
la fuerza de la mano, esto debido a que <strong>los jugadores de videojuegos no solo realizan el doble de movimientos de los brazos durante cada sesión, si no que los movimientos van dirigidos a un objetivo 
y no solo son ejercicios repetitivos</strong>.
