       	Este trastorno de la conducta, es de origen neurobologico se produce debido a una alteración
        del sistema nervioso central., y en la mayoría de los casos se presenta en niños (4 niños por cada niña) 
        durante la infancia, siendo hasta un 75% de los casos de origen genético,  hiperactividad (movimiento 
        continuo y superior a lo esperado para la edad del niño), falta de atención e impulsividad.
        <br /><br />
        Por lo regular quienes la padecen suelen aislarse del resto del grupo ya que por su actitud impulsiva y
        en algunas ocasiones violenta, les es difícil adaptarse a las formas y reglas de juego, convirtiéndose
        también en una de las principales causas de fracaso escolar, dada la facilidad de distracción, la falta
        de concentración y la tendencia a no finalizar las actividades que inicia y evitar las que requieren de 
        esfuerzo continuo.
 <br />
<br />       
<h2>Indicadores de hiperactividad según la edad del niño</h2>

<br />
<ul>
<li type="circle"><b>De 0 a 2 años.</b></li>. Contracciones musculares durante el sueño, problemas en el ritmo 
	del sueño y durante la comida, períodos cortos de sueño y sobresaltos al despertar, resistencia a los cuidados
	habituales, reactividad elevada a los estímulos auditivos e irritabilidad. <br /><br />
<li type="circle"><b>De 2 a 3 años.</b></li>. Inmadurez en el lenguaje expresivo, actividad motora excesiva, escasa 
	conciencia de peligro y propensión a sufrir numerosos accidentes.<br /><br />
<li type="circle"><b>De 4 a 5 años.</b></li> Problemas de adaptación social, desobediencia y dificultades en el seguimiento de normas.<br /><br />
<li type="circle"><b>A partir de 6 años.</b></li> Impulsividad, déficit de atención, fracaso escolar, comportamientos antisociales y problemas de adaptación social.<br /><br />
</ul>
                                                            
<h2>Tratamiento del TDAH</h2>  
<br />
	El TDAH es una patología poco conocida, difícil de detectar y fácil de confundir con la falta de límites en el menor. La complicación de tipo neurológico se desata en edades
    comprendidas entre los 3 y los 4 años, alcanzando el estado más crítico a los 6 años de edad. Los especialistas señalan que los niños con hiperactividad no tratados a tiempo 
    tendrán problemas también durante la adolescencia, Sin embargo, un tratamiento continuado, a medida que el niño va creciendo, permitirá que el trastorno mejore e incluso que 
    se consiga controlar.
     <br />
    La gran dificultad que presentan los niños para atender, seleccionar, mantener, y controlar la atención a los estímulos que se les presentan, así como la excesiva agitación
    que presentan, justifican la necesidad de ayuda y de seguimiento profesional. Un especialista ayudará al niño a que adquiera hábitos y estrategias cognitivas para que su
    desarrollo social, familiar y escolar, esté a la altura de sus capacidades.
    <br />
    <br />
<h3>El tratamiento tiene como objetivo: </h3> 
<br />
    <ul>
        <li type="disc">Mejorar o anular los síntomas del trastorno </li>
        <li type="disc">Disminuir o eliminar los síntomas asociados </li>
        <li type="disc">Mejorar el aprendizaje, lenguaje, escritura, relación social y familiar. </li>
    </ul>
 	<br />
    <br />
    Para eso, el especialista empleará, según el caso, todos los medios a su alcance para facilitar información a los padres y profesores, tratamiento farmacológico (imprescindible 
    en 7 de cada 10 niños), y tratamiento psicopedagógico.  
    <br />
    <br />
    No se debe olvidar que los padres juegan un papel fundamental durante el tratamiento. Los niños hiperactivos necesitarán mucho apoyo, comprensión, cariño y, sobre todo, mucha paciencia 
    para que poco a poco consigan desarrollar su día a día con normalidad. 
