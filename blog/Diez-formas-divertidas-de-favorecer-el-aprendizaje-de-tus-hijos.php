<h2>1. Rompe moldes, literalmente. </h2>
<br />
Deja de comprar cajas con artilugios que prometen resultados espectaculares para 
aumentar la inteligencia del niño. En lugar de eso, déjate guiar por el propio niño. Tómate tiempo para observar 
qué le interesa, y podrás empezar a ver tu entorno de una forma nueva.
<br />
<br />
<h2>2. Organiza un viaje a tu propio jardín o al parque de al lado.</h2>
<br />
Es estupendo salir a visitar una granja o al zoo, pero no hace falta ir tan lejos para despertar el cerebro de tu hijo. 
Los niños reciben una gran cantidad de estímulos intelectuales en su propio jardín o en el parque del barrio, donde pueden 
ser testigos del milagro de las briznas de hierba que se mecen con el viento, con las casas que construyen las hormigas, 
y toda la abundante vida que bulle en el suelo y la vegetación.
<br />
<br />
<h2>3. Fíjate en los números: </h2>
<br />
Están por todas partes. Igual que puedes ver rectángulos en los edificios, o círculos, triángulos y hexágonos en las señales 
de tráfico, los números están por todas partes y aparecen a cada paso en la vida de los niños. Cuando el niño reparte las 
patatas fritas con sus amigos, o comprueba que hay suficiente pastel para todo el mundo, está aprendiendo matemáticas.
<br />
<br />
<h2>4. Anima al niño a aprender el sentido de los números en su contexto.</h2>
<br/>
Todos aprendemos mejor cuando aprendemos algo significativo. Un niño aprende más sobre el valor del dinero si tiene la
 oportunidad de ganarse unas monedas en un puesto de refrescos, y ve qué puede comprarse con ellas, de lo que nunca aprenderá 
 haciendo fichas.
<br/>
<br/>
<h2>5. Enséñale a tu hijo que leer es divertido.</h2>
<br/>
Si compartes tu entusiasmo por la lectura y el niño te ve absorto/a leyendo un libro o un periódico, le estarás mostrando de
 forma indirecta la importancia y el disfrute de la lectura.
<br/>
<br/>
<h2>6. Practica la lectura dialógica. </h2>
<br/>
No basta con leerle al niño. Pídele al niño que piense en un final distinto, hablad del libro desde su propia experiencia.
<br/>
<br/>
<h2>7. No insistas en que hay una sola manera correcta de hacer algo. </h2>
<br/>
Si tu hijo encuentra una solución nueva para un problema, ¡fenomenal!
<br/>
<br/>
<h2>8. Permite que tenga tiempo y espacio para sí mismo.</h2>
<br/>
A veces los niños necesitan desconectar de los amigos y estar a su aire. Puede parecer como si no estuvieran haciendo “nada”,
 pero en realidad cuando “no hacen nada” también están aprendiendo muchísimo. Los niños necesitan poder ser espontáneos.
<br/>
<br/>
<h2>9. Deja que tu hijo lleve la batuta. </h2>
<br/>
Si jugáis juntos y tú participas controlando demasiado el juego, el niño perderá interés y tú habrás perdido la oportunidad de 
permitirle imaginar y crear.
<br/>
<br/>
<h2>10. Apúntate a la diversión. </h2>
<br/>
Unirse al juego de los niños puede ser el mayor reto al que los padres se puedan enfrentar. ¡Pero vale la pena!
<br/>
