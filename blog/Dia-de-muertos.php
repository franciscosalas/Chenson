		<div class="sub" >Inicios de la festividad…</div>
        <br />
        Desde épocas prehispánicas la muerte ha desempeñado un papel protagónico  dentro de la sociedad mexicana, donde incluso 
        ha sido adorada como una deidad por diversas culturas.
        <br />
        Existen indicios de que esta celebración se ha llevado a cabo por aproximadamente tres mil años, por etnias como la maya y
        la mexica, en donde se realizaban rituales para celebrar la vida de sus ancestros.
        
        <br />  <br />
		<div class="sub">En la actualidad…</div>
        <br />
		En un interesante contraste entre el dolor que representa  perder que un ser amado y el ambiente colorido que caracteriza 
        la mayoría de las festividades mexicanas, el día de muertos se celebra el día dos de noviembre, convirtiéndose en una de las 
        festividades nacionales más emblemáticas que incluso ha logrado trascender a nivel internacional, por lo que ha sido declarada 
        Patrimonio Cultural Inmaterial de la Humanidad por la Unesco.
	    
        <div class="sub">Día de muertos en Michoacán</div>
        <br />
        En medio de un ambiente oscuro y solemne, se lleva a cabo una de las celebraciones más representativas de la festividad, la noche 
        del 1 al 2 de noviembre podemos ver el lago de patzcuaro alumbrado por la luz de las veladoras de cientos de turistas y lugareños que
         presentan sus ofrendas y su canto convirtiendo la isla de Janitzio en un gran altar en tributo a los que ya no están.
        <br />
        
        Algo característico de la cultura de los mexicanos, es ese aire de cierta sátira e ironía ante la muerte, convirtiéndola en tradicionales 
        dulces creativos versos cargados de humor y hermosas ilustraciones de la tan querida y a la vez temida catrina.
        <br />
        <div class="sub">Alumbrando el camino a los seres queridos que ya no están</div>
         <br />
         <center>
         <div id="pietextos">
         Con la creencia de que ese día los seres queridos vuelven del mas allá para convivir nuevamente con los familiares y amigos que dejaron 
         atrás,  se celebra con un altar en el que se incluyen elementos que fueron del agrado del ser amado, como sus alimentos y bebidas favoritos,
          flores cuyo  aroma sirve de guía, veladoras que alumbran el camino, papel picado que representa la alegría de tener de vuelta al ser querido, 
          son algunos de los elementos básicos que debe incluir un tradicional altar éste día de muertos.
          </div>
          </center>
