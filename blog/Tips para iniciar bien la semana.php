        <br />
       Después de dos reconfortantes días de descanso, el Lunes se ha convertido históricamente en el día más odiado de la semana, el problema es que iniciar mal con el lunes nos puede llevar a tener un mal martes y un peor miércoles, por lo que es importante iniciar la semana con una actitud positiva, levantarte con un ánimo distinto y tratar de disfrutar de tu día, a fin de cuentas que el lunes solo es un día más ¿no? 
        
        <br />  <br />
		<div class="sub">Tómatelo con calma</div>
        <br />
		Importante comenzar con un buen desayuno, un baño refescante, un poco de ejercicio al aire libre, o cualquier cosa que te permita iniciar el día relajado, dale a tu cuerpo tiempo para ajustarse después del fin de semana, si comienzas estresado tanto el día como la semana completa te parecerán eternos…
          <br />  <br />
          <div class="sub">Asegúrate de verte bien</div>
        <br />
		El verte bien ayudará a mejorar tu estado de ánimo, asegúrate de que el mundo se entere de que te ves tan bien como te sientes ;)
         <br /><br />
         <div class="sub">Selecciona un buen playlist</div>
        <br />
		La música es un elemento importantísimo para mantener un buen estado de ánimo a lo largo del día, estudios demuestran que una buena canción tiene mejor efecto que diversas técnicas de relajación, selecciona una lista que te ponga de buenas, lo peor que puede pasar es que termines consiguiendo pareja de baile 
          <br /><br />
          <div class="sub">Organiza una escapada el día lunes…</div>
        <br />
		Saber que al final del día te espera hacer algo divertido, te ayudará a sobrellevar de mejor manera el día más temido de la semana, Sal a comer algo rico, a ver una película, pasear con la familia, o porque no hasta una copa, no olvides regresar a casa temprano si no lo que tendrás al día siguiente será un nuevo lunes esperando por ti.
          <br /><br />
          <div class="sub">Come <a href="http://www.chenson.com.mx/blog/6%20Alimentos%20para%20activar%20tu%20cerebro.php">Chocolate</a></div>
        <br />
		Es una deliciosa alternativa de mantenerte feliz durante el día, pues favorece la producción de endorfinas sustancias del cerebro que te hacen sentirte bien, libera el estrés y activa el ánimo además de su propiedades antioxidantes, pero recuerda ingerirlo en cantidades pequeñas, por más difícil que parezca. 
