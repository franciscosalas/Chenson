       El proceso de la conquista se convierte en todo un arte fácil de concretar para algunos, toda una 
       odisea para la mayoría, lo cierto es que no existe una formula conocida que nos garantice el éxito, 
       sea con la persona que acabas de conocer, o con aquella que tanto te ha gustado desde hace años.
        <br /><br />
       Entrando en el mes del amor, te traemos algunos tips que si bien no te convertirán en todo un Don Juan, 
       definitivamente te ayudaran a no sucumbir en el intento
        <br /><br />
		<h1>Encuentra la persona indicada</h1>
<br />
       Definitivamente el primer paso es detectar la persona indicada, sea por edad, gustos en común, atracción física, 
       química, actitud, etcétera. Si lo que quieres es llegar a algo más, es importante elegir a alguien que no tenga 
       pareja y mucho menos esposo o esposa.
<br /><br />
	<h1>En la escuela u oficina</h1>	
<br />
		Elegir a alguien dentro del circulo que habitas diariamente tiene sus ventajas, dado que el tiempo de exposición 
        y posibilidades de encuentro son mucho mayores lo que  permitirá un acercamiento paulatino, “Se creativ@” puedes 
        iniciar con una simple pregunta de rutina, después vendán las invitaciones y lo que sigue…
<br /><br />
<h1>De fiesta</h1>
<br />
        El ambiente, la música, la luz tenue y porque no algunas copas, se convierten en el material apropiado para desinhibirte, 
        eso sin mencionar la posibilidad de que la persona que elegiste también se encuentre ahí con la idea de conocer a alguien.
<br />	<br />
<h1>La prima de la amiga del conocido</h1>
<br />	
Cuando la amiga de tu prima o el primo de la conocida se unen al plan de salida, viaje o comida se generan las condiciones ideales 
para ligar, por la simple eliminación de que podrías ser la única opción posible. Recuerda que a menor oferta, mayor demanda. Además
 el espacio de convivencia atenúa la indecisión de hablarle o no, pues en algún momento tendrán que hacerlo momento ideal para demostrar 
 tu simpatía.
<br /><br />
<h1>En el transporte público</h1>
<br />	
El ambiente casual y el ajetreo del viaje se convierten en el ambiente ideal para demostrar caballerosidad cediendo el asiento o simplemente 
compartir comentarios sobre “cualquier cosa” si existe algo en común y llevas la conversación de una manera natural, quizá termine por darte 
su facebook, twitter y porque no hasta su teléfono.	
<br /><br />
<h1>Internet es tu aliado</h1>
<br />
Si en alguno de los anteriores escenarios, lograste obtener su twitter o Facebook, mantente en contacto, te permitirá conocer más acerca de 
la persona, además amplía tus posibilidades de concretar una cita futura.	
<br /><br />
<h1>Comportate casual</h1>
<br />
El siguiente paso es el encuentro, este debe ser de lo más natural posible, o en su defecto lo suficientemente planeado para que sea realista
 y parezca que paso sin querer	
<br /><br />
<h1>La mirada importa</h1>
<br />
El contacto visual es básico, es muy importante que se dé incluso antes del contacto físico, busca que no sea ni tan intenso ni tan tímido, 
siempre acompañado de una sonrisa.
<br />	<br />	
<h1>Demuestrale que te interesa</h1>
<br />
Una vez que comience a fluir la conversación, debes poner toda la atención posible, ya que cualquier comentario suyo puedes usarlo a tu favor
 para continuar la plática en caso de silencios incómodos.	
 <br /><br />
<h1>Si te van a presentar...</h1>
<br />		
			
Si tu prospecto es alguien que te van a presentar, no está de más hacer una investigación previa, con el fin de conocer algunos de sus gustos y 
actividades, aparte de ayudarte a saber si en verdad tienen intereses en común, te ayudará a volver más interesante la conversación.


