        El estrés es la respuesta fisiológica de defensa del organismo ante una situación de tensión, el cual busca 
        adaptarse y reajustarse ante presiones tanto internas, como externas.
        <br /><br />
        El estrés definitivamente no es una enfermedad exclusiva de los adultos, ya que <strong>un reciente estudio realizado por la 
        UNAM resulto con un 34% de niños en etapa de primaria, con niveles de medio a alto de estrés.</strong>
        <br /><br />
<h1>Las principales causas que provocan crisis de estrés en esta etapa suelen ser:</h1>
<br />
       <ul>
        <li type="circle">Fracaso escolar</li>
        <li type="circle">Fallecimiento, separación o abandono de alguna persona cercana</li>
        <li type="circle">Sentimiento de abandono o de no ser querido</li>
        <li type="circle">Fracaso en las relaciones con otros niños/as</li>
        <li type="circle">Carencia de afectividad</li>
        <li type="circle">Ausencia de modelos y valores sociales</li>
        <li type="circle">Insuficiente tiempo para jugar o relajarse</li>
        <li type="circle">Altos niveles de exigencia escolar y personal</li>
        <li type="circle">Excesiva dependencia de televisión, videojuegos e internet</li>
        <li type="circle">Malos hábitos de alimentación, higiene y salud</li>
       </ul>
<br />
	<h1>Como identificarlo</h1>	
<br />
		<ul>
        	<li type="circle">Disminución del apetito y otros cambios en los hábitos alimenticios</li>
            <li type="circle">Orinar en la cama</li>
            <li type="circle">Dolor de cabeza</li>
            <li type="circle">Frecuentes sobresaltos al dormir</li>
            <li type="circle">Pesadillas o Sueños agitados</li>
            <li type="circle">Ansiedad</li>
            <li type="circle">Falta de alegría</li>
            <li type="circle">Preocupaciones</li>
            <li type="circle">Incapacidad de relajarse</li>
            <li type="circle">Miedos nuevos o recurrentes (miedo a la oscuridad, a estar solo o a los extraños, etc.)</li>
            <li type="circle">Aferrarse al adulto, incapaz de perderlo de vista</li>
            <li type="circle">Inseguridad</li>
            <li type="circle">Incapacidad para socializar</li>
            <li type="circle">Rabia en exceso o que no es congruente con la situación</li>
            <li type="circle">Llanto por motivos sin importancia</li>
            <li type="circle">Incapacidad para controlar sus emociones</li>
            <li type="circle">Comportamiento agresivo o terco sin razón</li>
            <li type="circle">Regresión a comportamientos típicos de etapas anteriores del desarrollo</li>
            <li type="circle">Desinterés a participar en actividades familiares o escolares</li>
            <li type="circle">Alteraciones en el sueño</li>
            <li type="circle">Tartamudeo</li>
            <li type="circle">Molestia estomacal o dolor de estómago</li>
        </ul>	
<br />
<h1>Que puedes hacer como padre para ayudar a tu hijo:</h1>
<br />
        <ul>
        	<li type="circle">Dale al niño un hogar lleno de amor, 
            confianza y seguridad, donde el pueda expresar sus sentimientos y emociones.</li>
            <li type="circle">Ten mucho cuidado con lo que ven tus hijos en la televisión, evita que vea programas 
            que generen en ellos miedo, ansiedad o agresividad.</li>
            <li type="circle">Dedícale tiempo para compartir actividades que el disfruta.</li>
            <li type="circle">Crea un espacio de confianza donde el pueda expresar las preguntas que le 
            surge y donde pueda compartir contigo sus miedos y frustraciones.</li>
            <li type="circle">Escuchar al niño sin juzgarlo o criticarlo.</li>
            <li type="circle">Ayúdale a crear una solida autoestima donde reconozca que es un ser maravilloso
             y perfecto y que cuando se equivoca puede aprender y corregir sus equivocaciones.</li>
            <li type="circle">Revisa cuales son las decisiones que puedes permitirle tomar por sí solo, incítalo 
            a que lo haga, felicítalo por los buenos resultados y ayúdalo a reflexionar cuando las cosas no salen como esperaba.</li>
            <li type="circle">La actividad física es indispensable para liberar el exceso de energía sobretodo en los niños.</li>
            <li type="circle">Conoce cuáles son las situaciones que generan estrés en tu hijo y mantente atento a cuando se presenten 
            para darle el apoyo y soporte que necesite.</li>
            <li type="circle">Identifica a tiempo cualquier síntoma de estrés en tu hijo.</li>
            <li type="circle">Antes de realizar cualquier cambio por más pequeño que sea infórmaselo a tu hijo. el es un miembro activo 
            de la familia, no lo excluyas por ser pequeño.</li>
            <li type="circle">Cuando sientas que la situación se te ha salido de las manos busca un profesional.</li>
            <li type="circle">Enséñale a tu hijo a relajarse y a ver los problemas acompañados de muchas soluciones.</li>
            <li type="circle">Exprésale claramente que cuenta con tu apoyo para lo que el necesite.</li>
        </ul>                             
