        <div id="textos2">
        
		El regreso a clases ya está cerca y con ello la compra de útiles para los peques que regresan a la escuela o entran por primera ves, lo que termina por convertirse en un verdadero dolor de cabeza, por lo que te presentamos algunos consejos publicados por la <strong>procuraduria federal del consumidor (Profeco)</strong><br /><br /><br />
        
<h1>Lápices y colores de madera</h1>
<br />
Que no tengan grietas, eso provoca que la punta se rompa. Asegúrate de que la goma esté bien adherida.<br /><br />
<h1>Gomas</h1>
<br />
Asegúrate que no se deshagan al usarlas, así durarán más tiempo.<br />

<br />
<h1>Crayones</h1>
<br />
  Que no estén porosos ni se deshagan al pintar.</p>
<br />

<h1>Juego de geometría y reglas</h1>
<br/>
Revisa que el material sea resistente, que no tenga rebabas en el contorno y que tanto números, como graduación sean legibles.<br/>
<br/>
<h1>Compás</h1>
<br/>
  Revisa que abra y cierre fácilmente y que sus piezas no se desprendan al usarlo.</p>
<br/>

<h1>Pegamento líquido</h1>
<br/>
Que la tapa selle perfectamente, que no esté seco y que la etiqueta indique el tipo de materiales para el que está hecho.
<br /><br />
<h1>Lápiz adhesivo</h1>
<br/>
Si está empaquetado que esté bien cerrado. Si puedes abrirlo revisa que no esté seco. Verifica que sea apto para el clima del lugar en que vives.<br/>
<br/>                            
<h1> Tijeras</h1>
<br/>
Que tengan punta 
redondeada para evitar accidentes. Su nombre correcto es “Tijera Escolar” o “Tijera Papelera Escolar”. Revisa que corte firmemente y que la etiqueta indique para qué materiales sirve.<br/><br/>
<h1>Bolígrafos</h1>
<br/>
Revisa que no dejen manchas al escribir, que no escurra la tinta y que su trazo sea uniforme.<br/>
<br/><br/>

 															
<h1>Cuadernos</h1>
<br/>
  Revisa que las pastas sean resistentes, al igual que las hojas. Checa que su espiral o grapas sujetadoras estén bien colocadas y sean lo suficientemente fuertes para durar. Recuerda que aquéllos con personajes de moda son más caros y no resultan más útiles.
  <br/><br/>

<h1>Mochila</h1>
<br/>
  Checa que tengan buenas costuras y acabados, que su material sea resistente a la intemperie, que tenga el tamaño indicado para tus útiles y que no sean muy pesadas.

  <br/><br/>
  
 <h1>Cintas adhesivas</h1>
 <br/>
Elige preferentemente rollos grandes para evitar pagar de más por muchos rollos pequeños. Revisa que el enrollado sea uniforme y sin ondulaciones. No siempre es necesario un despachador.
<br /><br /><br />
