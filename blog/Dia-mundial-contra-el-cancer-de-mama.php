        El cáncer de mama es el tumor más frecuente entre las mujeres occidentales, En la actualidad este padecimiento afecta a 230,000 mujeres tan solo en América. 
<br/><br/>
El día 19 de octubre celebramos el día mundial contra el cáncer de mama, con el fin de crear conciencia y sensibilizar a la población sobre esta enfermedad que causa miles de muertes anuales en todo el mundo y la importancia de la detección temprana para poder erradicar la enfermedad. 
<br/>
Los síntomas del cáncer de mama avanzado pueden abarcar:<br/>
•	Bulto en la mama<br/>
•	Aumento del tamaño de los ganglios linfáticos (adenopatías) en la axila<br/>
•	Cambios en el tamaño, textura de la piel o color de la mama<br/>
•	Enrojecimiento de la piel<br/>
•	Formación de depresiones o arrugas<br/>
•	Cambios o secreción por el pezón<br/>
•	Descamación<br/>
•	Tracción del pezón hacia un lado o cambio de dirección<br/>
•	Dolor óseo.<br/>
•	Dolor o molestia en las mamas.<br/>
•	Úlceras cutáneas.<br/>
•	Hinchazón de un brazo (próximo a la mama con cáncer).<br/>
•	Pérdida de peso.<br/>

<br/><br/>
Como diagnosticarlo a tiempo<br/>
Cinco sencillos pasos que te pueden salvar la vida:<br/>
1.	Tomar agua simple, comer verduras y frutas diariamente, llevando una alimentación correcta,<br/>
2.	Realizar ejercicio y hacer la vida activa, para lograr el peso ideal<br/>,
3.	Observar, tocar y sentir, ¡Si se detecta algún cambio en las mamas informárselo al personal de salud!<br/>
4.	Acudir a la unidad médica para que el personal de salud revise las mamas y<br/>
5.	Si se tiene entre 40 y 60 años, realizarse la mastografía bianualmente.<br/>
<br/><br/>
El cáncer de mama puede manifestarse como una tumoración asintomática y que cuando ya hay retracción 
de la piel sea un cáncer avanzado, por lo que al detectar una masa, la paciente debe buscar ayuda profesional
 y pedir un diagnóstico exacto basado en estudios y no en presunción clínica.
 <br/><br/>
Es importante mencionar que este tipo de cáncer, a diferencia de otros, es totalmente detectable y 
se puede prevenir a tiempo si la mujer está al tanto de sus síntomas y mantiene un programa permanente y 
sistemático de autoexploración a partir de los 20 años.



