       El 14 de Febrero está cada vez más cerca, ¿ya conseguiste una cita Con la persona que tanto te agrada? 
       Entonces sigue estos prácticos consejos para que no arruines tu primera cita….
        <br /><br />
		<h1>Selecciona bien el lugar</h1>
<br />
       Elige un territorio que los haga sentir cómodos a ambos, si la conversación no es lo tuyo,  el cine puede 
       ser una buena opción, ya que no tienes que hablar demasiado y al salir la película se convertirá en un buen 
       tema de conversación.
<br /><br />
	<h1>Cuidado con el vestuario</h1>	
<br />
		Toma en consideración el lugar al que acudirás para poder elegir con un atuendo formal o uno relajado, tan fuera 
        de lugar quedarás yendo de esmoquin a una fiesta rave, como con una bermuda a una boda.
<br /><br />
<h1>Compórtate como todo un caballero</h1>
<br />
        Acercar la silla, abrir la puerta del coche, pagar la cuenta, son detalles que jamás dejarán de ser bien vistos, 
        por lo que hay que conservarlos y llevarlos a cabo en todo momento.
<br />	<br />
<h1>Durante la plática…</h1>
<br />	
Es importante que a otra persona sepa más sobre ti, pero ten cuidado en no exagerar con el “YO”, es básico que le preguntes 
también sobre su vida…
<br /><br />
<h1>Sinceridad desde el inicio</h1>
<br />	
Una relación a largo plazo los llevará a conocerse como son en realidad, por lo que siempre es importante decir la verdad desde el principio, 
sobre todo no exageres detalles de tu vida solo para tratar de quedar bien.	
<br /><br />
<h1>Mantén despierto tu sexto sentido</h1>
<br />
Para saber si le gustas en necesario aprender a leer entre líneas, en el caso de las mujeres suele ocurrir que te digan algo que signifique exactamente 
lo contrario, puede estarte diciendo un NO cuando en realidad quiere decir un SI.	
<br /><br />
<h1>No mientas sobre tus "ex"</h1>
<br />
Un estudio realizado por Doug Kendrick en la universidad de Arizona, revelo que es igualmente ineficaz exagerar el número de conquistas anteriores, como mostrarse 
demasiado humilde al respecto.	
<br /><br />
<h1>Llama al siguiente día</h1>
<br />
Aunque también existen algunas teorías afirmando lo contrario, una llamada al día siguiente de la cita para comentar sobre lo bien que lo pasaste, puede reafirmar 
el interés del prospecto para una nueva cita
<br />	<br />	
<h1>Las redes sociales son un buen aliado</h1>
<br />
Algunas redes sociales como Facebook o Twitter, se convierten en herramientas sumamente útiles para mantener un contacto constante.	
 <br /><br />
<h1>Un regalo original</h1>
<br />		
			
Para la siguiente cita olvida los regalos convencionales como flores o chocolates, averigua que cosas le gustan y llévale un obsequio original y que sea de su agrado. 
