		El <b>desayuno</b> es la primer ingesta de energía de la jornada he ahí que se convierte en la comida más importante del día, es por ello que los 			expertos recomiendan que esta sea variada, tratando de incluir lacteos, cereales y frutas variadas lo que supone un <b>aporte importante de vitaminas, zinc, calcio y hierro y otros nutrientes que ayudarán a que el cuerpo funcione con mayor eficiencia</b>. por ello te presentamos<strong> 10 alimentos para iniciar mejor el día</strong>.
        <br /><br />
        
<h1>Yogurt</h1>
<br />
        <b>El yogur es una gran fuente de calcio y proteínas</b> que, además, puede servir como sustitutivo de la leche para aquellas personas que tienen problemas para digerir la lactosa, ya que en el proceso de elaboración del yogur ésta desaparece casi por completo. Aunque tiene más grasa, el yogur griego es una buena alternativa, pues contiene hasta el doble de proteínas que la versión convencional. Lo que hay que evitar en la medida de lo posible son los yogures azucarados y de sabores, que tienen mucho más azúcar del necesario. Una buena alternativa es tomar el yogur con fruta, que le aporta sabor y lo endulza sin elevar tanto las calorías.

<br /><br />
<h1>Huevos</h1>
<br />
       Los huevos han protagonizado en los últimos años uno de los mayores cambios en lo que a consejos nutricionales se refiere. En la década de los ochenta se extendió la creencia popular –alentada por algunos nutricionistas– de que los huevos tenían demasiado colesterol y era malo tomarlos a diario. En 2009 un estudio de la Universidad de Surrey (Reino Unido) afirmaba que la mayoría de la gente puede comer tantos huevos como lo desee sin temer efectos dañinos en la salud. Es cierto que el huevo contiene mucho colesterol pero también posee su propio antídoto, la lecitina, una grasa que favorece el control del colesterol evitando que se adhiera a las paredes arteriales y desplazándolo hacia los lugares donde se metaboliza.
<br />
Los huevos son, además, una <b>gran fuente de proteínas y vitamina D</b>. En el desayuno es recomendable tomarlos duros, pasados por agua o en revuelto. Si se toman fritos estaremos añadiendo un extra de grasa que debemos tener en cuenta.

<br />
<br />
<h1>Avena</h1>
<br />
        La avena es uno de los cereales más saludables, contiene beta-glucano, un tipo de fibra que ayuda a reducir el colesterol si se consume a diario. La avena además es rica en ácidos grasos omega-3, ácido fólico y potasio.
<br />
La avena se puede consumir en distintos preparados. Todos son saludables excepto aquellos que incluyan demasiado azúcar. Esto no quiere decir que tengamos que comer avena a secas, pero es mejor tomarla con miel o fruta o, al menos, controlando la cantidad de azúcar que añadimos

<br />
<br />
<h1>Plátanos</h1>
<br/>
      <b>No hay mejor momento para consumirlo que el desayuno, ya que se trata de una de las frutas más calóricas</b>, que no conviene tomar de noche. En el desayuno, por el contrario, es perfecto y puede emplearse como sustitutivo del azúcar en los cereales o el yogur. Se trata de un alimento rico en fibra, en forma de almidón resistente, y es recomendable para las personas con problemas de tensión pues contiene potasio, una sustancia que ayuda a mantener bajas las pulsaciones.
        
       
<br/>
<br/>
<h1>Café</h1>
<br/>
El café ha sido desde hace siglos el acompañamiento obligatorio de nuestros desayunos. Recientemente se han publicado varios estudios a favor y en contra de su consumo, y los efectos de la cafeína sobre nuestro cuerpo siguen siendo discutidos. La realidad es que el café puede tener efectos tanto positivos como negativos en sus consumidores, en función de los problemas de cada uno. Hay estudios que aseguran que el café puede ayudarnos a vivir más, y que es una gran fuente de antioxidantes que no debe ser subestimada, pero lo cierto es que las personas con hipertensión, migrañas o desórdenes del sueño deberían apartarlo de su dieta. Para el resto, el café sigue siendo el mejor aliado para empezar el día con energía.
<br/>
<br/>
<h1>Melón y sandía</h1>
<br/>
Son frutas con muy pocas calorías en proporción a su aporte nutritivo, contienen también mucha agua, lo que ayuda a hidratarnos correctamente. Ambas frutas constituyen una gran fuente de vitaminas C y la <b>sandía, además, contiene citrulina, un compuesto biológico que produce un relajamiento de los vasos capilares y ayuda a mejorar la circulación</b>.
														<br /><br />
						
<br/>
<br/>
<h1>Germen de trigo y linaza</h1>
<br/>
El germen de trigo y la linaza son dos complementos dietéticos que pueden añadirse a cualquier comida, ofreciendo un aporte extra muy recomendable en determinadas dietas. El germen de trigo constituye la parte viva del grano que contiene todo lo necesario para el crecimiento de la futura planta. Se trata de una gran fuente de vitamina E, un potente antioxidante (dos cucharadas aportan el 15% que se recomienda consumir a diario).
<br />
<b>La linaza es un popular complemento dietético cuyo consumo se recomienda a las personas con problemas de colesterol, aunque para quien es verdaderamente útil es para aquellas personas con predisposición a padecerlo</b> –tiene un efecto propiláctico, no terapeútico–. Las semillas del lino son muy ricas en ácidos grasos omega 3 y basta añadir dos cucharadas de estas a nuestro yogur o nuestro cereal para obtener el 100% de la cantidad diaria recomendada. La linaza se vende en semillas y molida.

<br/>
<br/>                            
<h1> Fresas, frambuesas y arándanos</h1>
<br/>
Tanto las fresas, como las frambuesas y los arándanos –y otras frutas similares como la mora– son bajas en calorías y tienen las mayores concentraciones de antocianinas, un pigmento natural responsable de su color rojo característico, que funciona como un potente antioxidante. En los últimos años se han realizado numerosos estudios sobre los beneficios del pigmento, que pasa directamente al torrente sanguíneo y tiene importante efectos antiinflamatorios, antidiabéticos, antitumorales, y mejora la agudeza visual y el comportamiento cognitivo.
<br />
Estas frutas, además, son ricas en vitamina C, ácido fólico y fibra, y pueden servir de acompañamiento perfecto para nuestros cereales o yogures. La temporada de estas frutas es muy concreta, y las frambuesas y arándanos son muy caros, por lo que, si se quiere gozar de sus beneficios todo el año sin que sufra nuestra economía, comprarlas congeladas es una buena alternativa.

<br/><br/>
<h1>Té</h1>
<br/>
Las propiedades del té han sido resaltadas en infinidad de estudios científicos. Se trata de una buena alternativa al café para aquellas personas a las que les sienta mal la cafeína y es una poderosa fuente de antioxidantes. En concreto el té verde parece ser la variedad con mayores beneficios para nuestra salud, gracias al galato de epigalocatequina, <b>un potente antioxidante que podría, incluso, ayudar a combatir el alzhéimer</b>.
<br/>
<br/>

 															
<h1>Jugo de naranja</h1>
<br/>
El jugo de naranja es uno de los complementos más populares de un buen desayuno, pero no siempre se consume correctamente. Para empezar, muchas de las propiedades del jugo recién exprimido se pierden en sus variedades comerciales. Entre éstas, no obstante, hay muchas diferencias. Como norma hay que huir del néctar (que no deja de ser un zumo aguado con muchísimo azúcar) y mirar bien el contenido en azúcar del jugo que compramos.
<br />
Aunque tomemos el zumo recién exprimido nunca hay que abusar de él, ni pensar que un solo vaso sirve para no tomar fruta el resto del día. El zumo de naranja tiene muchas calorías y azúcar y debe consumirse con moderación: basta con un vaso (pequeño) al día.

