        <div class= "titulos2">Hábitos de las personas <br />verdaderamente felices</div>
				
                
                <img src="imagenes/Happy_people-portada.png" align="center" height="250"/>
                </center>
                <br />
        <div id="textos2">
        
        Todos en algún momento de nuestras vidas o durante todas ellas buscamos el ser felices convirtiéndolo
         en nuestra meta principal, tratando de identificar aquellos detalles y momentos que dejan alguna marca 
         positiva durante nuestro camino, el ser feliz depende de diversas circunstancias del entorno, pero principalmente
         depende de nosotros, el realizar actividades u ocasionar situaciones que nos traigan satisfacción, ayudarán a 
         que la vida se lleve de mejor manera.
          <br />
		Tal es la importancia que le damos a ser felices, que hay infinidad de estudios que han tratado de definir qué
        factores influyen en una persona sea feliz, a continuación listamos algunos de los resultados de dichas 
        investigaciones:
		
        <br /><br />
        
<h2>Se rodean de personas felices</h2>
<br />
        Durante un estudio del Framingham hert study que llevo más de 20 años, encontraron que las personas que están rodeadas
        de gente feliz, tienen más probabilidades de ser felices en el futuro, <i><b>confirmando así que la felicidad realmente es 
        contagiosa</b></i>. 
<br />
<br />
<h2>Sonríen cuando Realmente lo sienten</h2>
<br />
Si bien el tener un pensamiento feliz incluso cuando no lo estás, puede ayudarte a sentirte mejor, estudios han comprobado que el 
<i><b> "falsear” </b></i>una sonrisa durante un momento difícil, lo único que hace es empeorar el estado de ánimo.
												<br />	<br />			
					<center><img src="imagenes/sonrisa-falsa.jpg" /></center>
<br />
<br />
<h2>Perseverancia</h2>
<br />
Según el psicólogo Peter Kramer, que la perseverancia es lo opuesto a la depresión y no la felicidad, un rasgo que distingue a una 
persona feliz, es la actitud para enfrentarse y reponerse ante una situación difícil.
<br />
<br />
<h2>Tratan de ser felices</h2>
<br/>
El simple hecho de intentar ser feliz, te hace más feliz. Estudios recientes han demostrado que aquellos que activamente tratan 
de ser felices, logran tener un mejor estado de ánimo permanente.
													<br /><br />
							<center>	<img src="imagenes/feliz.jpg" />	</center>
<br/>
<br/>
<h2>Celebran los pequeños logros</h2>
<br/>
Sin duda un gran triunfo nos hace más felices, pero la vida cotidiana está llena también de pequeños triunfos, como el entregar 
un trabajo bien hecho, preparar una rica comida para la familia, etc. El celebrar esos pequeños logros, sin duda hará que te 
sientas mejor.
<br/>
<br/>
<h2>Aprecian los placeres sencillos</h2>
<br/>
NSe toman el tiempo para disfrutar de pequeños placeres, desde el disfrutar de un buen café, una placentera caminata o 
escuchar una canción, darle sentido a esas pequeñas acciones y no solo a los grandes placeres, también ayudará a generar 
bienestar en el transcurso del día.
														<br /><br />
							<center>	<img src="imagenes/sandwich.jpg" />	</center>
<br/>
<br/>
<h2>Las personas felices ayudan a los demás </h2>
<br/>
Partiendo desde un círculo virtuoso, donde hacer feliz a alguien, implica que ese alguien pueda hacer feliz a otra persona, 
el ayudar a los demás aparte de ayudar a mejorar el entorno, el ayudar de distintas formas, dispara el centro de recompensa
 de nuestro cerebro, el cual es responsable de la euforia por la liberación de dopamina según reciente publicaciones.
<br/>
<br/>
<h2>Se dejan llevar por el momento</h2>
<br/>
El entregarse de lleno a  una actividad satisfactoria, que implique desafío, conlleva a perder la noción del tiempo, 
incluso de las personas que nos rodean, siempre y cuando sea de manera voluntaria y con un objetivo claramente dirigido al 
éxito.
<br/>
<br/>
<h2>Buscan ver el lado bueno de las cosas</h2>
<br/>
El ser optimista ante la adversidad, aparte de reducir el estrés, mejora nuestra tolerancia al dolor y según estudios recientes,
 mejora la salud de pacientes con padecimientos  cardiacos.
<br/>
<br/>
<h2>Disfrutan de una buena canción</h2>
<br/>
La música como lenguaje universal, se convierte en un buen estimulante para ser feliz, teniendo incluso mayor efecto que otras 
técnicas de relajación, el problema es que así como nos hace sentir felicidad, también puede llevarnos a la tristeza, por lo que
 es importante seleccionar bien la música que escuchamos en determinados momentos.
 															<br /><br />
        							<center>	<img src="imagenes/tocadiscos.jpg" />	</center>
<br/>
<br/>
<h2>Se ejercitan</h2>
<br/>
Se ha demostrado que el ejercicio alivia los síntomas de depresión, ansiedad y estrés, debido a que mientras te ejercitas el cerebro 
libera varias sustancias químicas que amplifican los sentimientos de felicidad y relajación.
															<br /><br />
									<center>	<img src="imagenes/ejercicio.jpg" />	</center>
<br/>
<br/>
<h2>Duermen bien</h2>
<br/>
Una buena noche de sueño ayuda a disminuir la ansiedad y los cambios negativos de humor, por lo que dormir bien mejorará el estado anímico.
															<br /><br />
									<center>	<img src="imagenes/dormir-bien.jpg" />	</center>
<br/>
<br/>
<h2>Ríen tanto como pueden</h2>
<br/>
La frase <i><b>“la risa es la mejor medicina”</b></i> no está para nada alejada de la realidad, al reír se liberan sustancias químicas que ayudan 
a reducir el dolor y el estrés, además de mejorar nuestro sistema inmunológico, controlar el apetito y disminuir los niveles de
 colesterol de manera similar al ejercicio físico.
 															<br /><br />
 									<center>	<img src="imagenes/riendo.jpg" />	</center>
