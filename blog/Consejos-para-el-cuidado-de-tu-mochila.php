       Tu <b>Mochila Chenson</b> va siempre contigo a donde vayas, permitiéndote llevar todo lo que necesitas 
       (y un poco más…) y aunque está <b>fabricada con los más altos estándares de calidad</b>, siempre es importante 
       tener algunos cuidados con ella para aumentar su tiempo de vida, por eso que traemos para ti algunos tips: 
        <br /><br />
	<h1>Limpieza</h1>	
	<br />
	Para limpiar manchas y suciedad, utiliza preferentemente una esponja húmeda y jabón, los movimientos bruscos 
    y roce de la lavadora pueden ocasionar daños en tu <b>Mochila</b>, por lo que se recomienda siempre lavarla a mano, 
    en caso de ser necesario utilizar la lavadora, se recomienda utilizar ciclos cortos y definitivamente evitar el
    centrifugado, es mejor permitir que seque al aire libre en lugares abiertos.	
    <br /><br />
	<h1>Cierres atorados o difíciles de abrir</h1>
	<br />
    Puedes utilizar spray de silicona, o algunos productos especializado a base de aceite, esto permitirá aumentar la
    vida útil de tus cierres y partes móviles, asegúrate también que no haya hilos sueltos o residuos que puedan obstruir 
    su paso.                          
    <br />	<br />		
    <h1>Procura no llevar sobrepeso innecesario</h1>
	<br/>    
    Si bien tu <b>Mochila Chenson</b> está diseñada para soportar bastante procura no llevar carga innecesaria, ya que esto además puede
     traerte problemas de salud.                        
	<br />	<br />
    <h1>Evita malos olores…</h1> 	
    <br />
    Si tus pequeños llevan comida en alguno de los compartimentos de la <b>mochila</b>, es importante  comprobar que se la han comido o sacarla 
    cuando vuelven del colegio. La comida abandonada y aplastada puede dejar mancha y olores en la <b>mochila</b>.	
    <br /><br />
    <h1>Prevén manchas difíciles de erradicar…</h1>
    <br />
    Aunque los materiales de tu <b>Mochila Chenson</b> son fácilmente lavables, comprueba que no queden bolígrafos o rotuladores abiertos,
     para evitar manchas muy difíciles de erradicar en este tipo de materiales plásticos.
