       	El amor es una de las emociones más buscadas por todos nosotros, 
        y no en vano ya que sus efectos pueden ir más allá de una simple sensación, 
        también nos pueden salvar la vida, así que “que esperas”, es momento de ir buscando 
        a quien amar…
 		<br/><br/>
        El fenómeno del amor es un conjunto de sensaciones que se experimentan a nivel físico y 
        mental, cuando alguien tiene una fuerte atracción física por otra persona, es un hecho 
        que no tiene edad, ni duración promedio, puede durar horas, días, meses o incluso años….
        <br/><br/>	
        Según asegura la psiquiatra Laura Romans Demaria, de la secretaria de salud (SSA), el amor 
        es un importante factor contra la depresión, ansiedad y enfermedades cardiovasculares, ya que
          la liberación de distintas sustancias en el cerebro de los enamorados provoca bienestar físico y 
          emocional.
        <br/><br/>	
        Estudios realizados por la SSA, señalan que el amor es un factor protector, establecer relaciones de 
        amor estables y duraderas, ya sea con una pareja, familiares o amigos genera mayor bienestar físico y 
        emocional.
        <br/><br/>				
		El estudio detalla que durante el enamoramiento suelen surgir sensaciones como: palpitaciones, taquicardia, 
        sudoración de manos o mariposas en el estómago, se dice que es amor a primera vista y realmente existe".
        <br/><br/>
        Agregó que este estado particular también puede manifestarse a través de alegría, emoción y excitación cuando 
        se está en compañía de la persona o se piensa en ella.
        <br/><br/>
        La especialista detalló que todo ello deriva de un complejo mecanismo fisiológico cerebral, que provoca 
        elevación en la producción de algunos neurotransmisores como la dopamina, que al liberarse genera sensación de 
        euforia, motivación e incremento de energía.
        <br/><br/>
        Otra de las sustancias cerebrales involucradas es la noradrenalina, que se relaciona directamente con la respuesta 
        fisiológica a través de sudor, taquicardia, palpitaciones, insomnio, hiperactividad, ansiedad y disminución del 
        apetito, entre otros.
        <br/><br/>
        En ese contexto, dijo que en la etapa del amor a largo plazo intervienen a nivel cerebral otro tipo de neuromoduladores 
        como la oxitocina y la vasopresina, que están relacionados con el apego y establecimiento de vínculos, lo que se 
        traduce en un amor estable.
        <br/><br/>
        El amor a largo plazo se da con el conocimiento del otro; la excitación y la euforia disminuyen; es un amor más apacible, 
        donde hay calma, seguridad y una sensación de unión consolidada", puntualizó.
