		<div class="sub" >Un poco de historia…</div>
        <br />
        Las fiestas de octubre nacen en la ciudad de Guadalajara en el año de 1965, convirtiéndose a la fecha en
        toda una tradición y una de las expresiones culturales más importantes del estado. Se trata de un festival
        recreativo y cultural cuya sede central se encuentra en el auditorio Benito Juárez, en la ciudad de Guadalajara 
        Jalisco, simultáneamente se llevan actividades culturales en diversas regiones del estado, destacando actuaciones 
        de la orquesta filarmónica de Jalisco, diversas compañías de danza contemporánea y folclórica, una gran variedad
        de puestas teatrales, la ya tradicional expo ganadera y demás actividades deportivas.
        <br />
        Como plato principal, el festival nos ofrece una gran cantidad de conciertos diarios que abarcan prácticamente todos 
        los géneros musicales y con artistas de renombre de talla internacional, de los cuales podemos disfrutar de forma 
        gratuita en el mismo auditorio además del ya tradicional palenque, donde nos encontraremos con grandes artistas nacionales
         con gran trayectoria.
        <br />  <br />
		<div class="sub">El banderazo inicial…</div>
        <br />
		Un colorido desfile por el centro de la ciudad, da inicio con las festividades, donde podemos disfrutar de carros alegóricos,
        payasos,  tablas gimnasticas, música y la presencia de varias celebridades.
	    <br />
        Durante la feria, podremos disfrutar también de una amplia excelente variedad gastronómica tradicional para deleitar a toda la
         familia con antojitos mexicanos de distintas regiones del país.	
        <br />
        También  podemos encontrar una gran cantidad de juegos mecánicos para todas las edades alumbran lugar retando al más temerario 
        ofreciendo una alta dosis de adrenalina, desde los clásicos carritos chocones, el carrusel hasta los juegos más extremos pasando 
        por una pista de hielo que podrá disfrutar toda la familia.
        <br /> <br />
        <div class="sub">Presentaciones especiales…</div>
        <br />
        Este año se presenta una interesante exhibición de cuerpos humanos titulada “Our body” en la cual podrémos apreciar distintos 
        cuerpos humanos reales así como sus distintos órganos.
        <br />
        Otra de las interesantes atracciones es la canica azul, que este año se encarga de regresarnos a la infancia con uno de los más 
        grandes artistas mexicanos de todos los tiempos, Gabilondo Soler “CRI-CRI”, en el recorrido nos encontraremos con sus personajes 
        más famosos, ambientando cada escena con las canciones que lo hicieron convertirse en el personaje favorito de los niños de varias generaciones.
        <br />
        Adicional a la feria realizada en el auditorio, se llevan a cabo eventos alternos en distintos recintos culturales de la ciudad de Guadalajara,
         donde podremos disfrutar de buen teatro, música y galas de ballet. 
        <br />
        Una vez más como cada año el festival del mes de octubre se convierte en una excelente propuesta para pasar un buen día acompañados de la familia.
